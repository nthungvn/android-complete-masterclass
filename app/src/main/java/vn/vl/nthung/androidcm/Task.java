package vn.vl.nthung.androidcm;

public class Task {
    private int id;
    private String name;

    public Task(String name) {
        this.id = id;
        this.name = name;
    }

    public Task(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
